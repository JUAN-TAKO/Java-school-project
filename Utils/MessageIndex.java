package Utils;

/**
 *
 * @author denicolt
 */
public class MessageIndex extends Message{
    private int index;
    
    public MessageIndex(MessageType m, int index){
        super(m);
        setIndex(index);
    }

    /**
     * @return the index
     */
    public int getIndex() {
        return index;
    }

    /**
     * @param index the index to set
     */
    public void setIndex(int index) {
        this.index = index;
    }
}

    
